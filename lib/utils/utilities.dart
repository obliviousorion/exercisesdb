import 'package:flutter/painting.dart';

class Utilities {
  static EdgeInsets padding() {
    return const EdgeInsets.symmetric(
      horizontal: 24,
      vertical: 16,
    );
  }
  static EdgeInsets containerPadding() {
    return const EdgeInsets.symmetric(
      horizontal: 12,
      vertical: 6,
    );
  }
}
